package com.iadtec.hackathon.DTO;

public class PaisResponse {

    private Long id;
    private String nome;

    public PaisResponse() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
